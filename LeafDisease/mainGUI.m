% --- Executes on button press in pushbutton3.
function pushbutton3_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton3 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
%Image Segmentation using Color Thresholder app
I9 = handles.ImgData2;
I = I9;
I7 = handles.ImgData1;
[bw,rgb] = background_removal(I7);
%Masking Green_Pixels

%2 is green, 3 is blue, 1 is red
mask = (rgb(:,:,2)>rgb(:,:,1))&(rgb(:,:,2)>rgb(:,:,3));

%Binary Singleton Expansion Function bsxfun
%applies the element-by-element binary operation
%   specified by the function
seg_img=bsxfun(@times,rgb,cast(imcomplement(mask),'like',rgb));

%% Feature Extraction
% Convert to grayscale if image is RGB
img = rgb2gray(seg_img);

% Evaluate the disease affected area
black = im2bw(seg_img,graythresh(seg_img));
%figure, imshow(black);title('Black & White Image');
m = size(seg_img,1);
n = size(seg_img,2);

zero_image = zeros(m,n); 
%G = imoverlay(zero_image,seg_img,[1 0 0]);

cc = bwconncomp(seg_img,6);
diseasedata = regionprops(cc,'basic');
A1 = diseasedata.Area;
sprintf('Area of the disease affected region is : %g%',A1);

I_black = im2bw(I,graythresh(I));
kk = bwconncomp(I,6);
leafdata = regionprops(kk,'basic');
A2 = leafdata.Area;
sprintf(' Total leaf area is : %g%',A2);

%Affected_Area = 1-(A1/A2);
Affected_Area = (A1/A2);
if Affected_Area < 0.1
    Affected_Area = Affected_Area+0.15;
end
sprintf('Affected Area is: %g%%',(Affected_Area*100))
Affect = Affected_Area*100;

% Create the Gray Level Cooccurance Matrices (GLCMs)
glcms = graycomatrix(img);

%Evaluate 10 features from the disease affected region only
% Derive Statistics from GLCM
stats = graycoprops(glcms,'Contrast Correlation Energy Homogeneity');

%contrast value Returns a measure of the intensity contrast between a pixel and its neighbor over the whole image.
Contrast = stats.Contrast;

Correlation = stats.Correlation;
Energy = stats.Energy;
Homogeneity = stats.Homogeneity;
Mean = mean2(seg_img);
Standard_Deviation = std2(seg_img);
Entropy = entropy(seg_img);
RMS = mean2(rms(seg_img));
%Skewness = skewness(img)
Kurtosis = kurtosis(double(seg_img(:)));
Skewness = skewness(double(seg_img(:)));

% Put the 10 features in an array
feat_disease = [Contrast,Correlation,Energy,Homogeneity, Mean, Standard_Deviation, Entropy, RMS, Kurtosis, Skewness];
I8 = imresize(seg_img,[300,400]);
axes(handles.axes3);
imshow(I8);title('Segmented ROI');
set(handles.edit14,'string',Affect);
set(handles.edit7,'string',Mean);
set(handles.edit8,'string',Standard_Deviation);
set(handles.edit9,'string',Entropy);
set(handles.edit10,'string',RMS);
set(handles.edit11,'string',Kurtosis);
set(handles.edit12,'string',Skewness);
set(handles.edit3,'string',Contrast);
set(handles.edit4,'string',Correlation);
set(handles.edit5,'string',Energy);
set(handles.edit6,'string',Homogeneity);
handles.ImgData3 = feat_disease;
handles.ImgData4 = Affect;
% Update GUI
guidata(hObject,handles);



